/** Map an indicator's `unit` string to its Figma icon kind. */
export function kindForUnit(unit: string): UnitKind {
  switch (unit) {
    case "%":
      return "percent";
    case "persons":
    case "thousand persons":
      return "persons";
    case "manat":
    case "thousand manat":
      return "manat";
    case "m¬≤":
      return "area";
    case "cases":
      return "cases";
    case "families":
      return "family";
    case "facilities":
      return "facility";
    default:
      return "percent";
  }
}

