"use client";

/**
 * Per-unit icon used next to the DataCard value (Figma 47:3188
 * "Data type"). Maps the indicator's `unit` string — or its `id` when
 * the unit alone is ambiguous (e.g. `cases` covers crimes AND marriages
 * /divorces) — to one of twelve Figma symbols (45:134 "Data type"
 * frame).
 *
 * The icon renders 18 × 18 by default (matches the Figma pill's inner
 * size). Each kind carries its own viewBox so the original 24×24 and
 * 32×32 source coordinates render at identical visual scale without
 * manual rescaling. Every literal `#2C2C2C` has been replaced with
 * `currentColor`; the outer pill-background rect that ships with the
 * Figma export is stripped (the DataCard already provides the pill).
 *
 * "Thousand" variants of units (`thousand persons`, `thousand manat`,
 * `thousand tonnes`) reuse the same icon as their base unit — the
 * compact value text already signals scale via M / B / mln / mlrd
 * suffixes, so a redundant K-badge on the icon would just add noise.
 */

import type { ReactElement, SVGProps } from "react";

export type UnitKind =
  | "percent"
  | "persons"
  | "manat"
  | "area"
  | "cases"
  | "family"
  | "facility"
  | "birth"
  | "wedding"
  | "divorce"
  | "goods"
  | "tonne";

/** Pure unit → icon mapping. Use `kindForIndicator()` when the indicator
 *  is known — it routes by id first (e.g. marriages → wedding) and only
 *  falls back to this. */
export function kindForUnit(unit: string): UnitKind {
  switch (unit) {
    case "%":
      return "percent";
    case "persons":
    case "thousand persons":
      return "persons";
    case "manat":
    case "thousand manat":
      return "manat";
    case "m²":
      return "area";
    case "cases":
      return "cases";
    case "families":
      return "family";
    case "facilities":
      return "facility";
    case "tonnes":
    case "thousand tonnes":
      return "tonne";
    default:
      return "percent";
  }
}

/** Indicator-id-aware routing — overrides unit-based mapping for cases
 *  where the unit string is ambiguous (marriages/divorces share `cases`
 *  with crimes; births/deaths/natural_increase share `persons` with
 *  labour stats; cargo_transported shares `thousand tonnes` with bulk
 *  output). Falls back to kindForUnit(). */
export function kindForIndicator(unit: string, indicatorId: string): UnitKind {
  const override = INDICATOR_OVERRIDES[indicatorId];
  return override ?? kindForUnit(unit);
}

const INDICATOR_OVERRIDES: Record<string, UnitKind> = {
  // Life events — both use a smiley/face symbolism that fits births but
  // would feel off for deaths; keep just `births` on the birth glyph,
  // route deaths / natural_increase to the persons icon.
  births: "birth",
  marriages: "wedding",
  divorces: "divorce",
  // Cargo (boxes) vs. raw tonnage (weight scale) — distinct glyphs from
  // the Tonne kind helps the picker read at a glance.
  cargo_transported: "goods",
};

export interface UnitIconProps extends SVGProps<SVGSVGElement> {
  kind: UnitKind;
  /** Pixel size of the rendered SVG. Defaults to 18 (matches Figma 47:3188). */
  size?: number;
  /** Fill / stroke colour (driven via `currentColor`). Defaults `#3c3c3c`. */
  tone?: string;
}

export function UnitIcon({
  kind,
  size = 18,
  tone = "#3c3c3c",
  ...rest
}: UnitIconProps) {
  const def = ICONS[kind];
  return (
    <svg
      width={size}
      height={size}
      viewBox={def.viewBox}
      fill="none"
      style={{ color: tone, display: "block" }}
      aria-hidden
      {...rest}
    >
      {def.body}
    </svg>
  );
}

